package com.nova.Owner.DTO;

public class ShopDetailsDTO {
	
	
	private Long shopId;
	
	private String shopName;
	
	private String shopAddress;
	
	private String shopPhone;
	
	private String shopLogo;
	
	private Long numberOfEmployes;
	
	private String photo;
	
	private String ratings;
	
	private String todayAppointment;
	
	

	public Long getShopId() {
		return shopId;
	}

	public void setShopId(Long shopId) {
		this.shopId = shopId;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public String getShopAddress() {
		return shopAddress;
	}

	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}

	public String getShopPhone() {
		return shopPhone;
	}

	public void setShopPhone(String shopPhone) {
		this.shopPhone = shopPhone;
	}

	public String getShopLogo() {
		return shopLogo;
	}

	public void setShopLogo(String shopLogo) {
		this.shopLogo = shopLogo;
	}

	public Long getNumberOfEmployes() {
		return numberOfEmployes;
	}

	public void setNumberOfEmployes(Long numberOfEmployes) {
		this.numberOfEmployes = numberOfEmployes;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getRatings() {
		return ratings;
	}

	public void setRatings(String ratings) {
		this.ratings = ratings;
	}

	public String getTodayAppointment() {
		return todayAppointment;
	}

	public void setTodayAppointment(String todayAppointment) {
		this.todayAppointment = todayAppointment;
	}

}
